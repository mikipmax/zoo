    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed

        try {

            bandera_encontrado = 0;
            if (!txtbuscar.getText().isEmpty()) {
                bandera_buscar = 1;
                this.tareas = Controlador_habitats.recuperarPorId(txtbuscar.getText());
                if (!tareas.isEmpty()) {
                    bandera_encontrado = 1;

                    DefaultTableModel dtm = (DefaultTableModel) elemento_tabla.getModel();
                    dtm.setRowCount(0);
                    for (int i = 0; i < this.tareas.size(); i++) {
                        String all_cont = "";
                        for (int j = 0; j < this.tareas.get(i).getContinente().length; j++) {
                            all_cont += this.tareas.get(i).getContinente()[j] + " ";

                        }
                        dtm.addRow(new Object[]{
                            this.tareas.get(i).getNombreHab(),
                            this.tareas.get(i).getClima(),
                            this.tareas.get(i).getTipoVeg(),
                            all_cont

                        });
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No se ha encontrado ninguna coincidencia\nPuede insertar la nueva especie ahora");
                }

            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }


    }//GEN-LAST:event_btnbuscarActionPerformed

    private void elemento_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elemento_guardarActionPerformed

        if (mod == 1) {
            //------------------mi forma de conseguir los items de continentes que ha seleccionado un usuario
            JCheckBox[] chb_i = {chb_1, chb_2, chb_3, chb_4, chb_5, chb_6}; //creo un array de tipo JCheckBox con mis 6 items
            ArrayList continentes = new ArrayList(); //creo una lista de contientes para almacenar solo los items q el usuario eligió
            for (int i = 0; i < chb_i.length; i++) { //voy recorriendo los items 
                if (chb_i[i].isSelected()) { //compruebo q cada item haya sido seleccionado
                    continentes.add(chb_i[i].getText()); //voy añadiendo a mi lista los items q el usuario eligió
                }
            }
            String[] cont_select = new String[continentes.size()]; // creo un array de string tal como lo defini en el pojo de habitats
            //convierto la lista en un array normalito 
            for (int i = 0; i < continentes.size(); i++) { //recorro la lista de contientes   
                cont_select[i] = (String) continentes.get(i); //almaceno en cada posición de mi array el elemnto  q existe en la lista de contientes
            }
            //---------------------fin de mi forma
            Controlador_habitats.editar(txtNe.getText(), txtNc.getText(), txtDe.getText(), cont_select);
            System.out.println(txtNe.getText());
            txtNe.setText("");
            txtNc.setText("");
            txtDe.setText("");
            txtDe.setEditable(false);
            txtNc.setEditable(false);
            txtNe.setEditable(false);

            for (JCheckBox chb_i1 : chb_i) {
                chb_i1.setSelected(false);
                chb_i1.setEnabled(false);
            }
            elemento_guardar.setEnabled(false);
            btn_cancelar.setEnabled(false);
            cb_crud.setEnabled(true);
            bandera_crear_editar = 0;
            cargar_lista_de_tareas();
        } else {
            if (!txtNe.getText().isEmpty()) {

                int ban_repetido = 0;
                this.tareas = Controlador_habitats.cargarArbol();
                for (Habitats tarea : tareas) {

                    if (txtNe.getText().equalsIgnoreCase(tarea.getNombreHab())) {

                        ban_repetido = 1;
                    }
                }
                if (ban_repetido == 0) {
                    this.guardar();
                    txtNe.setText("");
                    txtNc.setText("");
                    txtDe.setText("");
                    txtDe.setEditable(false);
                    txtNc.setEditable(false);
                    txtNe.setEditable(false);
                    JCheckBox[] chb_i = {chb_1, chb_2, chb_3, chb_4, chb_5, chb_6};

                    for (JCheckBox chb_i1 : chb_i) {
                        chb_i1.setSelected(false);
                        chb_i1.setEnabled(false);
                    }
                    elemento_guardar.setEnabled(false);
                    btn_cancelar.setEnabled(false);
                    cb_crud.setEnabled(true);
                    bandera_crear_editar = 0;
                } else {
                    JOptionPane.showMessageDialog(null, "La especie ya existe");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el primer campo al menos");
            }

        }

    }//GEN-LAST:event_elemento_guardarActionPerformed

    private void cb_crudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_crudActionPerformed
        try {
            switch (cb_crud.getSelectedItem().toString()) {
                case "Crear":

                    if (bandera_encontrado == 0 && bandera_buscar == 1) {
                        txtDe.setEditable(true);
                        txtNc.setEditable(true);
                        txtNe.setEditable(true);
                        JCheckBox[] chb_i = {chb_1, chb_2, chb_3, chb_4, chb_5, chb_6};
                        for (JCheckBox chb_i1 : chb_i) {
                            chb_i1.setEnabled(true);
                        }
                        elemento_guardar.setEnabled(true);
                        btn_cancelar.setEnabled(true);
                        elemento_guardar.setText("Insertar");
                        cb_crud.setEnabled(false);
                        bandera_buscar = 0;
                        bandera_crear_editar = 1;
                        mod = 0;
                    }

                    break;
                case "Editar":

                    if (bandera_encontrado == 1 && bandera_buscar == 1) {

                        txtNc.setEditable(true);
                        txtDe.setEditable(true);
                        JCheckBox[] chb_i = {chb_1, chb_2, chb_3, chb_4, chb_5, chb_6};

                        for (JCheckBox chb_i1 : chb_i) {
                            chb_i1.setEnabled(true);
                        }

                        elemento_guardar.setEnabled(true);
                        elemento_guardar.setText("Guardar");
                        btn_cancelar.setEnabled(true);
                        cb_crud.setEnabled(false);

                        Habitats es = this.tareas.get(0);

                        txtNe.setText(es.getNombreHab());
                        txtNc.setText(es.getClima());
                        txtDe.setText(es.getTipoVeg());
//------------------mi forma de conseguir los items almacenados de respecitvo usuario
                        String[] cont_selec = es.getContinente();

                        for (int i = 0; i < chb_i.length; i++) {
                            for (int j = 0; j < cont_selec.length; j++) {
                                if (chb_i[i].getText().equals(cont_selec[j])) {
                                    chb_i[i].setSelected(true);
                                }
                            }
                        }
//*-----------------------------------
                        mod = 1;
                        bandera_buscar = 0;
                        bandera_crear_editar = 1;
                    }

                    break;
                case "Listar":
                    this.cargar_lista_de_tareas();

                    bandera_buscar = 0;
                    break;

                case "Eliminar":
                    if (bandera_encontrado == 1 && bandera_buscar == 1) {

                        String idc = (String) elemento_tabla.getValueAt(0, 0);

                        tareas = Controlador_habitats.eliminar(idc);
                        DefaultTableModel dtm = (DefaultTableModel) elemento_tabla.getModel();
                        dtm.setRowCount(0);
                        for (int i = 0; i < this.tareas.size(); i++) {
                            String all_cont = "";
                            for (int j = 0; j < this.tareas.get(i).getContinente().length; j++) {
                                all_cont += this.tareas.get(i).getContinente()[j] + " ";

                            }
                            dtm.addRow(new Object[]{
                                this.tareas.get(i).getNombreHab(),
                                this.tareas.get(i).getClima(),
                                this.tareas.get(i).getTipoVeg(),
                                all_cont

                            });
                        }
                    }

                    bandera_buscar = 0;
                    break;
                default:
                    bandera_buscar = 0;
                    System.out.println("Listar");
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_cb_crudActionPerformed

