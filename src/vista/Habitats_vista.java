    private void guardar() {
        String nombre_hab = txtNe.getText();
        String clima = txtNc.getText();
        String tipoVeget = txtDe.getText();
        //------------------mi forma de conseguir los items de continentes que ha seleccionado un usuario
        JCheckBox[] chb_i = {chb_1, chb_2, chb_3, chb_4, chb_5, chb_6}; //creo un array de tipo JCheckBox con mis 6 items
        ArrayList continentes = new ArrayList(); //creo una lista de contientes para almacenar solo los items q el usuario eligió
        for (int i = 0; i < chb_i.length; i++) { //voy recorriendo los items 
            if (chb_i[i].isSelected()) { //compruebo q cada item haya sido seleccionado
                continentes.add(chb_i[i].getText()); //voy añadiendo a mi lista los items q el usuario eligió
            }
        }
        String[] cont_select = new String[continentes.size()]; // creo un array de string tal como lo defini en el pojo de habitats
        //convierto la lista en un array normalito 
        for (int i = 0; i < continentes.size(); i++) { //recorro la lista de contientes   
            cont_select[i] = (String) continentes.get(i); //almaceno en cada posición de mi array el elemnto  q existe en la lista de contientes
        }
        //---------------------fin de mi forma
        Controlador_habitats.guardarArbol(nombre_hab, clima, tipoVeget, cont_select);
        System.out.println("");
        cargar_lista_de_tareas();
    }

